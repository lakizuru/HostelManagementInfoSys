    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        //checks validity of username
        if (Validate.isUsernameValid(username.getText())) {

                String pass1 = String.valueOf(password1.getPassword());
                String pass2 = String.valueOf(password2.getPassword());

                //checks validity of passwords
                if (Validate.isPasswordValid(pass1, pass2)) {
                    
                    if (admin.isSelected()) {
                            accessLevel = "ADMIN";
                    }
                    else if (moder.isSelected()){
                            accessLevel = "MODER";
                    }


                    try {							
                            //Openning DB connection
                            Class.forName(Database.dbDriver);
                            Connection connection = DriverManager.getConnection(Database.dbURL, Database.dbUsername, Database.dbPassword);
                            Statement statement = connection.createStatement();

                            //SQL INSERT statements for new Guests
                            String queryNewLogin = "INSERT INTO login (username, password, accountType) VALUES (?,?,?)";

                            //Prepared Statement Queries
                            PreparedStatement psNewLogin = connection.prepareStatement(queryNewLogin);
                            psNewLogin.setString(1, username.getText());
                            psNewLogin.setString(2, pass1);
                            psNewLogin.setString(3, accessLevel);

                            //Executing Prepared Statements
                            psNewLogin.execute();

                            //Closing DB Connection
                            connection.close();
                    }
                    catch (SQLIntegrityConstraintViolationException existingInfo) {
                            JOptionPane.showMessageDialog(null, "You have entered existing user's information \n" + existingInfo, "Invalid Inputs!", JOptionPane.ERROR_MESSAGE);
                            System.exit(-1);
                    }
                    catch (Exception dbError) {
                            JOptionPane.showMessageDialog(null, "Task Failed! \n" + dbError, "Database Error", JOptionPane.ERROR_MESSAGE);
                            System.exit(-1);
                    }
                }
                }
    }//GEN-LAST:event_submitButtonActionPerformed

